package j2se;
//Program about Datatypes and values
public class Variables {
	public static void main(String[] args) {
		short my_variable = 10;// int value
		float my_decimal = (float) 4.5;// float value
		double my_double = 11.56;// decimal value
		char my_char = 'A';// Singlechar only
		boolean is_true = false;// true or false

		System.out.println(my_variable);
		System.out.println(my_decimal);
		System.out.println(my_double);
		System.out.println(my_char);
		System.out.println(is_true);

	}
}
